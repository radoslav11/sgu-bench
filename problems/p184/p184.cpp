#include <bits/stdc++.h>

using namespace std;

template<typename T1, typename T2>
ostream& operator<<(ostream& out, const pair<T1, T2>& x) {
    return out << x.first << ' ' << x.second;
}

template<typename T1, typename T2>
istream& operator>>(istream& in, pair<T1, T2>& x) {
    return in >> x.first >> x.second;
}

template<typename T>
istream& operator>>(istream& in, vector<T>& a) {
    for(auto& x: a) {
        in >> x;
    }
    return in;
};

template<typename T>
ostream& operator<<(ostream& out, const vector<T>& a) {
    for(auto x: a) {
        out << x << ' ';
    }
    return out;
};

int64_t p, milk, c;
int64_t k, r, v;

void read() {
    cin >> p >> milk >> c;
    cin >> k >> r >> v;
}

void solve() {
    // Each patty needs k flour, r milk, v cabbage. The number of patties is
    // limited independently by each ingredient, so the answer is the minimum of
    // the integer divisions p / k, milk / r and c / v.

    int64_t answer = p / k;
    answer = min(answer, c / v);
    answer = min(answer, milk / r);
    cout << answer << '\n';
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int T = 1;
    // cin >> T;
    for(int test = 1; test <= T; test++) {
        read();
        // cout << "Case #" << test << ": ";
        solve();
    }

    return 0;
}
